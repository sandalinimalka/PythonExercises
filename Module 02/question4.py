number1_str = input("Enter a First Number: ")
number2_str = input("Enter a Second Number: ")
number3_str = input("Enter a Third Number: ")

number1 = int(number1_str)
number2 = int(number2_str)
number3 = int(number3_str)

summation = (number1 + number2 + number3)
product = (number1 * number2 * number3)
average = ((number1 + number2 + number3) / 3)

print("Sum is " + str(summation))
print("Product is " + str(product))
print("Average is " + str(average))

